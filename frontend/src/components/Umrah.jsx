    <p className=''>Umrah consists of four essential practices. Firstly, you get changed into the Ihram garments before the Miqat, perform two Rakahs of Salah and make your Niyyah as the Miqat approaches, thereafter reciting the Talbiyah frequently. Secondly, you perform Tawaf al-Umrah of the Kaaba followed by two Rakahs of Salah, preferably near Maqam Ibrahim. Thirdly, you do Sa’i of Safa and Marwa. Lastly, you shave (Halq) or shorten (Taqsir) your hair, allowing you to leave the state of Ihram and complete your Umrah.</p>
